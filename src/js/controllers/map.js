function initMap() {
    var myLatLng = {
        lat: 4.7256582,
        lng: -74.088149
    };

    // Create a map object and specify the DOM element for display.
    var map = new google.maps.Map(document.getElementById('cts-GoogleMap'), {
        center: myLatLng,
        zoom: 16,
        styles: CTSMap_Style
    });
    


    var icon = {
        url: "/img/map/googlemaps_marker.svg", // url
        scaledSize: new google.maps.Size(135, 60), // scaled size
          origin: new google.maps.Point(0,0), // origin
    anchor: new google.maps.Point(30,70),
    };

    var marker = new google.maps.Marker({
        position: myLatLng,
        title: "Calle 130 No 90-21, Rincon de Suba",
        icon: icon
    });
    marker.setMap(map);



}
